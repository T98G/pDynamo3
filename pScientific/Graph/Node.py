"""Graph node."""

from pCore import AttributableObject

#===================================================================================================================================
# . Class.
#===================================================================================================================================
class Node ( AttributableObject ):
    """A graph node."""

    def __lt__ ( self, other ):
        """Ordering."""
        if ( hasattr ( self, "index" ) and hasattr ( other, "index" ) ): return ( self.index < other.index ) # . Remove.
        else: return ( id ( self ) < id ( other ) )

