/*----------------------------------------------------------------------------------------------------------------------------------
! . Integer GCD.
!---------------------------------------------------------------------------------------------------------------------------------*/
Integer Integer_GCD ( Integer a, Integer b )
{
    Integer t ;
    if    ( a <  b ) { t = b ; b = a     ; a = t ; }
    while ( b != 0 ) { t = b ; b = a % b ; a = t ; }
    return a ;
}
